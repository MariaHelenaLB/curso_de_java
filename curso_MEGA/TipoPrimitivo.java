public class TipoPrimitivo {
	
	public static void main(String[] args) {
		
		double precoProduto = 42.6;
		
		boolean alunoMatricula = true;
		boolean alunoMatricula2 = false;
		
		//smepre utilizar ''
		char turma = 'A';
		char ano = '3';
		char simbolo = '@';
		
		long populacaoUberlandia = 650000;
		System.out.println(populacaoUberlandia);
		
		long populacaoMundial = 7000000000L;//todo literal que tem casas decimais é do tipo double, se colocar o L indica que é um literal do tipo long
		System.out.println(populacaoMundial);
		
		float saldoConta = 1030.43F;//todo literal que tem casas decimais é do tipo double, se colocar o F indica que o literalé do tipo float
		System.out.println(saldoConta);
		
	}
	
}