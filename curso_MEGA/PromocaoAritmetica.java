public class PromocaoAritmetica {
	
	public static void main(String[] args){
		int x = 10;
		int y = 5;
		int z = x + y;
		System.out.println(z);
		
		//outro exemplo:
		int a = 10;
		long b = 5;
		long c = a + b;
		System.out.println(c);
		
		long d = 10;
		float e = 9.34f;
		float f = d * e;
		System.out.println(f);
		
		int g = 3;
		float h = 2;
		float i = g / h;
		System.out.println(i);
		
		int j = 3;
		int k = 2;
		float l = j / (float) k;
		System.out.println(l);
	}
	
}
